package com.libbs.track.modelo.produto;

public class Biotecnologia {

    private String areaAtuacao;
    private String tecnologiaUtilizada;
    private String instituicao;
    private String nivelPesquisa;
    private String finalidade;

    public String getAreaAtuacao() {
        return areaAtuacao;
    }

    public void setAreaAtuacao(String areaAtuacao) {
        this.areaAtuacao = areaAtuacao;
    }

    public String getTecnologiaUtilizada() {
        return tecnologiaUtilizada;
    }

    public void setTecnologiaUtilizada(String tecnologiaUtilizada) {
        this.tecnologiaUtilizada = tecnologiaUtilizada;
    }

    public String getInstituicao() {
        return instituicao;
    }

    public void setInstituicao(String instituicao) {
        this.instituicao = instituicao;
    }

    public String getNivelPesquisa() {
        return nivelPesquisa;
    }

    public void setNivelPesquisa(String nivelPesquisa) {
        this.nivelPesquisa = nivelPesquisa;
    }

    public String getFinalidade() {
        return finalidade;
    }

    public void setFinalidade(String finalidade) {
        this.finalidade = finalidade;
    }
}
